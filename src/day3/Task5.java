package day3;

public class Task5 {

    public static void main(String[] args) {
        int i = 1;

        do {
            System.out.println("Tekrarlayan mesaj " + i);
            i++;
        } while (i <= 5);
    }

}


// Bir mesajı "do-while" döngüsü kullanarak ekrana 5 kez yazdıran bir Java programı yazın.